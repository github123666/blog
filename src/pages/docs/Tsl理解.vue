+ 浏览器通过向web服务器要ca证书
+ web服务返回证书和公钥
+ 浏览器验证ca里面颁发机构是否可信(是否为自生成或第三方),以及是否是该域名的证书，如果以上满足则会将会话密钥通过公钥加密。
+ web服务器通过私钥解密出会话密钥。

