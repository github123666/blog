ssl是套接字安全协议,tsl(传输层安全协议)是对ssl进行进一步完善的协议,主要用于用户与服务器之间安全通信。   
ssl基本已经被淘汰了,目前tsl基本取代了ssl。目前tsl也被称之为ssl/tsl。   
Tsl主要用于http数据加密
